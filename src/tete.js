// object recursive dive
const deepKeys = (obj) => {
  const keys = _.keys(obj);
  const result = keys.flatMap((key) => {
    if (!_.isObject(obj[key])) return key;
    return [key, deepKeys(obj[key])];
  });
  return result;
};

